private fun star(x : Int, y : Int, n : Int, bw : BufferedWriter){
    if((x/n)%3 ==1 && (y/n)%3 == 1){
        bw.write(" ")
    }else{
        if(n/3==0){
            bw.write("*")
        }else{
            star(x, y, n/3, bw)
        }
    }
}


